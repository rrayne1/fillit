#include "../includes/fillit.h"

void	ft_sqinit(t_mas *mas)
{
	int	i;
	int	j;

	i = 0;
	while (i <= 14)
	{
		j = 0;
		while (j <= 14)
		{
			mas->sq[i][j] = '\0';
			j++;
		}
		i++;
	}
}
